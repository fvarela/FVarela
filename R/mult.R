#' Multiplicación de dos números
#'
#' Función que realiza la multiplicación entre dos números
#'
#' @param a Primer número
#' @param b Segundo número
#'
#' @return La multiplicación entre a y b
#' @export
#'
#' @seealso \link{div}
#'
#' @examples
#' mult(5,4)

mult <- function(a,b) {
  return(a*b)
}
